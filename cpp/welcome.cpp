/* std is a namespace and  ::  is the scope resolution operator */
/* cout is a object present in std namespace which outputs the string on the console screen */
int main()
{
    std::cout << "Welcome to world of c++\n" << std::endl; /* endl is a manipulator defined in std namespace*/
    std::cout << "bye\n";
    std::cout << "size of int:" << sizeof(unsigned long);
    return 0;

}
